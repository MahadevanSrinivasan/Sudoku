%ComputePossibleValues
function [possibleRowValues, possibleColValues, possibleCellValues, possibleValues] = ComputePossibleValues(Problem, rowInd, colInd, cellInd)

    cellDefinitions = [1:3,10:12,19:21; ...
                   28:30,37:39,46:48; ...
                   55:57,64:66,73:75; ...
                   4:6,13:15,22:24; ...
                   31:33,40:42,49:51; ...
                   58:60,67:69,76:78; ...
                   7:9,16:18,25:27; ...
                   34:36,43:45,52:54; ...
                   61:63,70:72,79:81];
    allowedValues = 1:9;
    
    possibleRowValues = setdiff(allowedValues, Problem(rowInd,:));
    possibleColValues = setdiff(allowedValues, Problem(:,colInd));
    possibleCellValues = setdiff(allowedValues, Problem(cellDefinitions(cellInd,:)));
    possibleValues = intersect(possibleRowValues,possibleColValues);
    possibleValues = intersect(possibleValues,possibleCellValues);
    
end